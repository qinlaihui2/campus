USE campus_assistant;

-- 表情包表
CREATE TABLE emoji (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(50) NOT NULL COMMENT '表情名称',
    emoji_char VARCHAR(10) COMMENT 'Unicode表情字符（默认表情用）',
    image_url VARCHAR(500) COMMENT '图片URL（贴图表情用，存MinIO路径）',
    tags VARCHAR(200) COMMENT '搜索标签，逗号分隔',
    category VARCHAR(50) NOT NULL DEFAULT '默认' COMMENT '分类/表情包名称',
    sort_order INT NOT NULL DEFAULT 0 COMMENT '排序',
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    INDEX idx_category (category),
    INDEX idx_tags (tags)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='表情包表';

-- 用户收藏的表情
CREATE TABLE emoji_favorite (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    user_id BIGINT NOT NULL,
    emoji_id BIGINT NOT NULL,
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    UNIQUE INDEX idx_user_emoji (user_id, emoji_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='用户收藏表情';

